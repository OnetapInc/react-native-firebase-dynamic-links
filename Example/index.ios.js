import React, { Component } from 'react';
import {
  AppRegistry,
  StyleSheet,
  Text,
  View,
  Toast
} from 'react-native';
import FirebaseDynamicLink from 'react-native-firebase-dynamic-links';

export default class Example extends Component {
  

  componentWillMount () {
    FirebaseDynamicLink.addListener(this._handleLinkReceived);
  }

  componentWillUnmount () {
    FirebaseDynamicLink.removeListener();
  }

  _handleLinkReceived = (link) => {
    if (link) {
      Toast.show(link, Toast.LONG);
    }
  }

  render() {
    return (
      <View style={styles.container}>
        <Text style={styles.welcome}>
          Welcome to React Native!
        </Text>
        <Text style={styles.instructions}>
          To get started, edit index.ios.js
        </Text>
        <Text style={styles.instructions}>
          Press Cmd+R to reload,{'\n'}
          Cmd+D or shake for dev menu
        </Text>
      </View>
    );
  }
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
    justifyContent: 'center',
    alignItems: 'center',
    backgroundColor: '#F5FCFF',
  },
  welcome: {
    fontSize: 20,
    textAlign: 'center',
    margin: 10,
  },
  instructions: {
    textAlign: 'center',
    color: '#333333',
    marginBottom: 5,
  },
});

AppRegistry.registerComponent('Example', () => Example);
